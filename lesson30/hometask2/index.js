/*
 * successPromise должен зарезолвить число 67
 * Ответьте себе на вопрос, какой тип данных имеет переменная successPromise // promise Obj
 */

const successPromise = new Promise(resolve => {
  resolve(67);
});

/*
 * допишите обработчик успешного промиса (аргументы и тело ф-ции onSuccess)
 * чтобы в консоль вывелся квадрат числа, которое резолвиться из successPromise
 */

successPromise.then(function onSuccess(number) {
  const res = number ** 2;
  console.log(res);
});

/*
 * в обработчике ошибок промиса (ф-ция onError внутри .catch()) выведите в консоль текст 'I am an error'
 * текст вывелся в консоль?
 * подумайте почему: - нет не выведется,
 * 1.так как мы не задали  функцию для ошибки когда объявляли promise.
 * 0. у нас ресолв просто передает число, И ДАЖЕ ЕСЛИ  МЫ ОбЪЯВИЛИ РЕДЖЕКТ: мы после объявления сразу вызвали catch он не отработал бы ...
 *    * отработа бы в том случае: если результат ресолв зависил бы от Сторонних подгрузок в ходе выполнения фу-и
 *  т.е. если ресолв не выполнился то отработал бы реджект.
 *  имеем - при  таком описании промиса ошибка не выпадет -никогда )
 */
successPromise.catch(function onError(error) {
  console.log(error);
});
